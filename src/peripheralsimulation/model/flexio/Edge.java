package peripheralsimulation.model.flexio;

/**
 * Enum representing the edge type for a FlexIO shifter.
 *
 * This enum is used to specify the edge type for the shifter's operation,
 * including none, positive edge, and negative edge.
 *
 * @author Veronika Lenková
 */
public enum Edge {

	/** No edge detection */
	NONE,
	/** Positive edge detection */
	POSEDGE,
	/** Negative edge detection */
	NEGEDGE

}
